public class OperadoresAsignacion {

    public static void main(String[] args) {

        //Simple
        var numero = 10;

        int miNumero;
        miNumero = 15;

        //compuesto
        // += -= *= /= %=
        miNumero += 5;
        System.out.println("miNumero = " + miNumero);

        miNumero *= 2;
        System.out.println("miNumero = " + miNumero);
        

        //Variables multiples

        int a = 10, b = 15, c = 20;
        System.out.printf("a = %d, b = %d, c = %d", a,b,c);



    }
}

