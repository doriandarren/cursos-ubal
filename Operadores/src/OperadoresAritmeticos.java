public class OperadoresAritmeticos {
    public static void main(String[] args) {

        int a = 5, b = 3, resultado;

        //Suma
        resultado = a + b;
        System.out.println("resultado = " + resultado);

        // Resta
        resultado = a - b;
        System.out.println("resultado = " + resultado);

        // Multiplicacion
        resultado = a * b;
        System.out.println("resultado = " + resultado);

        //Division
        resultado = a / b;
        System.out.println("resultado = " + resultado);


        //Modulo
        resultado = a % b;
        System.out.println("resultado = " + resultado);



    }
}
