public class OperadoresComparacion {

    public static void main(String[] args) {

        int a = 3, b =2;

        // ==
        var resultado = a == b;
        System.out.println("resultado = " + resultado);

        // !=
        resultado = a != b;
        System.out.println("resultado = " + resultado);


        resultado = a > b;
        System.out.println("resultado = " + resultado);

        resultado = a >= b;
        System.out.println("resultado = " + resultado);

        resultado = a < b;
        System.out.println("resultado = " + resultado);

        resultado = a <= b;
        System.out.println("resultado = " + resultado);


    }

}
